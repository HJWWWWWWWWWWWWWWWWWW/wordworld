"""技能等级里程碑设计。

所有技能都通过元素与原始效果生成 Lv3/Lv5 强化。这样新增技能时也会自动
获得完整设计，不需要维护一份容易漏项的技能 ID 白名单。
"""
from typing import Any, Dict


ATTACK_MASTERY = {
    "火": {
        3: ("余烬", "命中附加2回合灼烧，每回合造成技能基础攻击加成20%的伤害。"),
        5: ("焚灭", "引爆目标身上的灼烧，并令本次伤害提高25%。"),
    },
    "冰": {
        3: ("寒侵", "命中使敌人减速20%，持续2回合。"),
        5: ("永冻", "25%概率冻结敌人，使其跳过下一次行动。"),
    },
    "雷": {
        3: ("雷痕", "无视敌人15%防御。"),
        5: ("天罚", "本次伤害提高20%，并有20%概率使敌人跳过下一次行动。"),
    },
    "风": {
        3: ("回风", "施放后返还20%斗气消耗，并额外获得1层连击。"),
        5: ("无影", "本次伤害提高15%，行动条推进50%。"),
    },
    "木": {
        3: ("生息", "命中后恢复自身5%最大生命。"),
        5: ("共生", "吸取本次实际伤害的20%恢复生命。"),
    },
    "土": {
        3: ("岩甲", "施放后获得自身8%最大生命的护盾。"),
        5: ("山崩", "本次攻击无视敌人30%防御，并使其防御降低10%。"),
    },
    "毒": {
        3: ("蚀骨", "额外施加1层中毒，中毒上限提升至7层。"),
        5: ("万毒噬心", "按目标当前中毒层数提高伤害，每层提高8%。"),
    },
    "无": {
        3: ("凝练", "斗气消耗降低20%，并使本次伤害提高10%。"),
        5: ("返璞归真", "暴击率提高25%，暴击伤害提高至2.5倍。"),
    },
}

UTILITY_MASTERY = {
    3: ("运转自如", "斗气消耗降低25%，原有效果提高25%。"),
    5: ("圆满之境", "原有效果提高50%，施放后行动条推进50%。"),
}


def build_skill_mastery(skill: Dict[str, Any], element: str = "无") -> Dict[int, Dict[str, str]]:
    """为任意技能生成 Lv3/Lv5 强化设计。"""
    rules = ATTACK_MASTERY.get(element, ATTACK_MASTERY["无"])
    if "atk:+" not in str(skill.get("effect", "")):
        rules = UTILITY_MASTERY
    return {
        level: {"name": name, "description": description}
        for level, (name, description) in rules.items()
    }
