# ============== PROCESSING ==============
$stats = @()
$totalBefore = 0
$totalAfter = 0
$stillShort = @()

foreach ($f in $files) {
    $path = Join-Path $dir $f
    if (-not (Test-Path $path)) { Write-Host "NOT FOUND: $f"; continue }

    $content = Get-Content $path -Raw -Encoding UTF8

    # Count Chinese chars
    $origCount = [regex]::Matches($content, "[一-鿿㐀-䶿豈-﫿]").Count
    $totalBefore += $origCount

    # Apply word replacements
    foreach ($term in $replacements.Keys) {
        $content = $content -replace [regex]::Escape($term), $replacements[$term]
    }

    # Add expansion if exists
    if ($expansions.ContainsKey($f)) {
        $content += "`r`n`r`n"
        foreach ($para in $expansions[$f]) {
            $content += $para + "`r`n`r`n"
        }
    }

    $newCount = [regex]::Matches($content, "[一-鿿㐀-䶿豈-﫿]").Count
    $totalAfter += $newCount

    # Write UTF-8 without BOM
    $utf8 = New-Object System.Text.UTF8Encoding $false
    [System.IO.File]::WriteAllText($path, $content, $utf8)

    if ($newCount -lt 1500) {
        $stillShort += [PSCustomObject]@{File=$f; Count=$newCount}
    }

    Write-Host "$f : $origCount → $newCount chars (added $($newCount - $origCount))"
}

Write-Host "`n========== SUMMARY =========="
Write-Host "Total before: $totalBefore"
Write-Host "Total after:  $totalAfter"
Write-Host "Added total:  $($totalAfter - $totalBefore)"

if ($stillShort.Count -gt 0) {
    Write-Host "`nWARNING - Still under 1500:"
    foreach ($s in $stillShort) { Write-Host "  $($s.File) : $($s.Count) chars" }
} else {
    Write-Host "`nAll 57 chapters now have 1500+ pure Chinese characters!"
}

Write-Host "`nDone."
