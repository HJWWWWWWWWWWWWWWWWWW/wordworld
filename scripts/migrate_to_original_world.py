def preview_replacements(text: str) -> List[Tuple[str, str]]:
    """预览文本中将要被替换的匹配项。"""
    changes = []
    for old, new in REPLACEMENTS:
        if old in text:
            changes.append((old, new))
    return changes


def apply_replacements(text: str) -> Tuple[str, int]:
    """对文本执行所有术语替换，返回 (新文本, 替换次数)。"""
    count = 0
    result = text
    for old, new in REPLACEMENTS:
        n = result.count(old)
        if n > 0:
            result = result.replace(old, new)
            count += n
    return result, count


def process_file(filepath: Path, dry_run: bool = True) -> int:
    """处理单个文本文件。返回替换次数。"""
    try:
        with open(filepath, "r", encoding="utf-8") as f:
            original = f.read()
    except UnicodeDecodeError:
        print(f"  ⚠️  跳过二进制文件: {filepath.relative_to(PROJECT_ROOT)}")
        return 0

    new_text, count = apply_replacements(original)

    if count == 0:
        return 0

    if dry_run:
        rel = filepath.relative_to(PROJECT_ROOT)
        print(f"\n  📄 {rel} ({count} 处替换)")
        changes = preview_replacements(original)
        for old, new in changes[:5]:
            print(f"      「{old}」→「{new}」")
        if len(changes) > 5:
            print(f"      ... 等 {len(changes)} 种术语")
    else:
        with open(filepath, "w", encoding="utf-8") as f:
            f.write(new_text)
        rel = filepath.relative_to(PROJECT_ROOT)
        print(f"  ✅ {rel} ({count} 处)")

    return count


def process_xlsx(filepath: Path, dry_run: bool = True) -> int:
    """处理 Excel 配置文件。"""
    try:
        import openpyxl
    except ImportError:
        print("  ⚠️  openpyxl 未安装，跳过 Excel 处理。")
        return 0

    if dry_run:
        rel = filepath.relative_to(PROJECT_ROOT)
        print(f"\n  📊 {rel} (预览模式)")
        wb = openpyxl.load_workbook(filepath, rich_text=False)
        total_count = 0
        for sheet_name in wb.sheetnames:
            ws = wb[sheet_name]
            sheet_count = 0
            for row in ws.iter_rows():
                for cell in row:
                    if cell.value and isinstance(cell.value, str):
                        changes = preview_replacements(cell.value)
                        if changes:
                            sheet_count += len(changes)
            if sheet_count > 0:
                print(f"      Sheet「{sheet_name}」: ~{sheet_count} 处")
            total_count += sheet_count
        wb.close()
        print(f"      总计约 {total_count} 处")
        return total_count

    # 实际执行
    wb = openpyxl.load_workbook(filepath, rich_text=False)
    total_count = 0
    for sheet_name in wb.sheetnames:
        ws = wb[sheet_name]
        sheet_count = 0
        for row in ws.iter_rows():
            for cell in row:
                if cell.value and isinstance(cell.value, str):
                    new_val, n = apply_replacements(cell.value)
                    if n > 0:
                        cell.value = new_val
                        sheet_count += n
        if sheet_count > 0:
            print(f"  ✅ Sheet「{sheet_name}」: {sheet_count} 处")
        total_count += sheet_count

    # 备份
    import shutil
    backup = filepath.with_suffix(".xlsx.bak")
    if not backup.exists():
        shutil.copy2(filepath, backup)
        print(f"  📦 已备份: {backup.name}")

    wb.save(filepath)
    wb.close()
    print(f"  ✅ {filepath.relative_to(PROJECT_ROOT)} (总计 {total_count} 处)")
    return total_count


def main():
    parser = argparse.ArgumentParser(description="去 IP 化迁移脚本")
    parser.add_argument("--apply", action="store_true",
                        help="实际执行（默认预览）")
    parser.add_argument("--target", choices=["src", "xlsx", "docs", "all"],
                        default="all", help="目标范围")
    args = parser.parse_args()

    dry_run = not args.apply
    if dry_run:
        print("🔍 预览模式 — 不修改文件。--apply 执行。\n")
    else:
        print("🔧 执行模式 — 将修改文件。\n")

    total = 0

    # ── src/ 目录 ──
    if args.target in ("src", "all"):
        print("═══ 源代码 ═══")
        src_dir = PROJECT_ROOT / "src"
        py_files = sorted(src_dir.rglob("*.py"))
        src_total = 0
        for f in py_files:
            if "__pycache__" in f.parts:
                continue
            n = process_file(f, dry_run)
            src_total += n
        print(f"  源代码合计: {src_total} 处\n")
        total += src_total

    # ── 文档 ──
    if args.target in ("docs", "all"):
        print("═══ 文档 ═══")
        docs_total = 0
        for name in ["README.md", "CLAUDE.md"]:
            f = PROJECT_ROOT / name
            if f.exists():
                n = process_file(f, dry_run)
                docs_total += n
        print(f"  文档合计: {docs_total} 处\n")
        total += docs_total

    # ── Excel ──
    if args.target in ("xlsx", "all"):
        print("═══ Excel 配置表 ═══")
        xlsx = PROJECT_ROOT / "story" / "text_game_event_schema_v4.xlsx"
        if xlsx.exists():
            n = process_xlsx(xlsx, dry_run)
            total += n
        else:
            print(f"  ⚠️  找不到 {xlsx}")
        print()

    if dry_run:
        print(f"🏁 预览完成: ~{total} 处待替换。")
        print("   python scripts/migrate_to_original_world.py --apply")
    else:
        print(f"🏁 迁移完成: {total} 处已替换。")


if __name__ == "__main__":
    main()
